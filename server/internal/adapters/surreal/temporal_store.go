// QueryTemporalRange returns all changes between two commits for a repo.
func (a *SurrealAdapter) QueryTemporalRange(ctx context.Context, repoSlug, fromCommit, toCommit string) ([]types.TemporalChange, error) {
	params := map[string]any{
		"repo_ref": models.NewRecordID("repo", repoSlug),
	}

	// Query nodes introduced or modified between the two commits
	var changes []types.TemporalChange

	tables := []string{"`function`", "class", "file"}
	for _, table := range tables {
		q := fmt.Sprintf(`SELECT * FROM %s WHERE repo = $repo_ref
			AND (introduced_commit IS NOT NONE OR last_modified_commit IS NOT NONE)
			ORDER BY last_modified_at DESC LIMIT 100;`, table)

		results, err := surrealdb.Query[[]nodeRow](ctx, a.db, q, params)
		if err != nil {
			continue
		}
		if results == nil || len(*results) == 0 {
			continue
		}

		for _, r := range (*results)[0].Result {
			node := rowToCodeNode(r, kindFromTable(table))
			change := types.TemporalChange{
				CommitHash: node.LastModifiedCommit,
				Timestamp:  safeTime(node.LastModifiedAt),
			}
			if node.IntroducedCommit == node.LastModifiedCommit {
				change.NodesAdded = append(change.NodesAdded, node)
			} else {
				change.NodesModified = append(change.NodesModified, node)
			}
			changes = append(changes, change)
		}
	}

	return changes, nil
}

// NodeHistory returns the temporal history of a specific node.
func (a *SurrealAdapter) NodeHistory(ctx context.Context, nodeID string) ([]types.TemporalChange, error) {
	table, localID := splitRecordID(nodeID)
	if table == "" || localID == "" {
		return nil, nil
	}

	const q = `SELECT * FROM type::record($id);`
	results, err := surrealdb.Query[[]nodeRow](ctx, a.db, q, map[string]any{
		"id": models.NewRecordID(table, localID),
	})
	if err != nil {
		return nil, fmt.Errorf("node history %s: %w", nodeID, err)
	}
	if results == nil || len(*results) == 0 || len((*results)[0].Result) == 0 {
		return nil, nil
	}

	r := (*results)[0].Result[0]
	node := rowToCodeNode(r, kindFromTable(table))

	var changes []types.TemporalChange
	if node.IntroducedCommit != "" {
		changes = append(changes, types.TemporalChange{
			CommitHash: node.IntroducedCommit,
			Timestamp:  safeTime(node.IntroducedAt),
			NodesAdded: []types.CodeNode{node},
		})
	}
	if node.LastModifiedCommit != "" && node.LastModifiedCommit != node.IntroducedCommit {
		changes = append(changes, types.TemporalChange{
			CommitHash:    node.LastModifiedCommit,
			Timestamp:     safeTime(node.LastModifiedAt),
			NodesModified: []types.CodeNode{node},
		})
	}

	return changes, nil
}

// EdgesIntroducedAt returns edges introduced in a specific commit.
func (a *SurrealAdapter) EdgesIntroducedAt(ctx context.Context, repoSlug, commitHash string) ([]types.CodeEdge, error) {
	params := map[string]any{"commit": commitHash}

	var edges []types.CodeEdge
	edgeTables := []string{"calls", "data_flow"}
	for _, table := range edgeTables {
		q := fmt.Sprintf("SELECT * FROM %s WHERE introduced_commit = $commit LIMIT 100;", table)
		results, err := surrealdb.Query[[]edgeRow](ctx, a.db, q, params)
		if err != nil {
			continue
		}
		if results == nil || len(*results) == 0 {
			continue
		}
		for _, r := range (*results)[0].Result {
			edges = append(edges, r.toCodeEdge(types.EdgeKind(table)))
		}
	}

	return edges, nil
}

// safeTime dereferences a time pointer, returning zero time if nil.
func safeTime(t *time.Time) time.Time {
	if t == nil {
		return time.Time{}
	}
	return *t
}

// edgeRow for temporal edge queries.
type edgeRow struct {
	In               *models.RecordID  `json:"in"`
	Out              *models.RecordID  `json:"out"`
	CallSite         string            `json:"call_site"`
	CallType         string            `json:"call_type"`
	IsDynamic        bool              `json:"is_dynamic"`
	IntroducedCommit string            `json:"introduced_commit"`
	Metadata         map[string]string `json:"metadata"`
}

func (r edgeRow) toCodeEdge(kind types.EdgeKind) types.CodeEdge {
	fromID := ""
	toID := ""
	if r.In != nil {
		fromID = fmt.Sprintf("%s:%v", r.In.Table, r.In.ID)
	}
	if r.Out != nil {
		toID = fmt.Sprintf("%s:%v", r.Out.Table, r.Out.ID)
	}
	return types.CodeEdge{
		Kind:             kind,
		FromID:           fromID,
		ToID:             toID,
		CallSite:         r.CallSite,
		CallType:         r.CallType,
		IsDynamic:        r.IsDynamic,
		IntroducedCommit: r.IntroducedCommit,
		Metadata:         r.Metadata,
	}
}
