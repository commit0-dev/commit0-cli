// UpsertNodeTemporal upserts a node with temporal (commit) metadata.
func (a *SurrealAdapter) UpsertNodeTemporal(ctx context.Context, node *types.CodeNode, commitHash string, commitTime time.Time) error {
	table := nodeTable(string(node.Kind))
	_, localID := splitRecordID(node.ID)
	if localID == "" {
		localID = node.Qualified
	}

	const q = `UPDATE type::record($id) MERGE {
		introduced_commit:    IF introduced_commit IS NONE THEN $commit ELSE introduced_commit END,
		introduced_at:        IF introduced_at IS NONE THEN $ts ELSE introduced_at END,
		last_modified_commit: $commit,
		last_modified_at:     $ts
	};`

	_, err := surrealdb.Query[any](ctx, a.db, q, map[string]any{
		"id":     models.NewRecordID(table, localID),
		"commit": commitHash,
		"ts":     commitTime,
	})
	if err != nil {
		return fmt.Errorf("upsert node temporal %s: %w", node.ID, err)
	}
	return nil
}

// UpsertEdgeTemporal upserts an edge with temporal metadata.
func (a *SurrealAdapter) UpsertEdgeTemporal(ctx context.Context, edge *types.CodeEdge, commitHash string, commitTime time.Time) error {
	table := string(edge.Kind)
	if table == "function" {
		table = "`function`"
	}

	const q = `UPDATE type::record($id) MERGE {
		introduced_commit: IF introduced_commit IS NONE THEN $commit ELSE introduced_commit END,
		introduced_at:     IF introduced_at IS NONE THEN $ts ELSE introduced_at END
	};`

	// Edge IDs in SurrealDB are typically auto-generated by RELATE.
	// We construct an approximate ID from the edge kind + from/to.
	edgeID := fmt.Sprintf("%s:%s->%s", edge.Kind, edge.FromID, edge.ToID)
	_, localID := splitRecordID(edgeID)
	if localID == "" {
		localID = edgeID
	}

	_, err := surrealdb.Query[any](ctx, a.db, q, map[string]any{
		"id":     models.NewRecordID(table, localID),
		"commit": commitHash,
		"ts":     commitTime,
	})
	if err != nil {
		// Non-fatal: edges may not have direct IDs
		a.log.Debug("upsert edge temporal failed", "edge", edgeID, "err", err)
	}
	return nil
}

// MarkNodeRemoved marks a node as removed at a specific commit.
func (a *SurrealAdapter) MarkNodeRemoved(ctx context.Context, nodeID, commitHash string, commitTime time.Time) error {
	table, localID := splitRecordID(nodeID)
	if table == "" || localID == "" {
		return nil
	}

	const q = `UPDATE type::record($id) MERGE {
		removed_commit: $commit
	};`

	_, err := surrealdb.Query[any](ctx, a.db, q, map[string]any{
		"id":     models.NewRecordID(table, localID),
		"commit": commitHash,
	})
	if err != nil {
		return fmt.Errorf("mark node removed %s: %w", nodeID, err)
	}
	return nil
}

